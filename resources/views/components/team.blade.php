                <div class="bio-column">
                    <p>
                        Gayane Umerova is dedicated to developing the culture sector
                        in Uzbekistan.
                    </p>
                    <br />
                    <p>
                        Head of the Department of Creative Economy and Tourism of the
                        Administration of the President of the Republic of Uzbekistan
                        and Chairperson of the Uzbekistan Art and Culture Development
                        Foundation (ACDF), Gayane Umerova is at the helm of building
                        Uzbekistan’s cultural infrastructure. Her efforts are bringing
                        the nation’s art, artists, and cultural heritage into the
                        global spotlight. Currently, she is overseeing the restoration
                        and development of the Centre for Contemporary Arts in
                        Tashkent, poised to become a new cultural hub for the region,
                        and is the commissioner of the Bukhara Biennial (5 September -
                        20 November 2025). She has spearheaded the inaugural Aral
                        Culture Summit (April 4-6, 2025); is driving the construction
                        of the new Uzbekistan National Museum designed by Tadao Ando
                        and is leading the forthcoming 43rd session of the UNESCO
                        General Conference that will take place in Samarkand on 30
                        October - 13 November 2025. She is the commissioner for the
                        Uzbekistan Pavilion at the Venice Biennale Arte and
                        Architettura since 2021 as well as for Uzbekistan’s
                        participation in Expo 2025 Osaka, among other significant
                        projects.
                    </p>
                </div>
                <div class="bio-column bio-column-secondary">
                    <p>
                        Committed to boosting Uzbekistan’s prominence on the
                        international culture scene, Umerova serves as the Chairperson
                        of the National Commission of Uzbekistan on UNESCO Affairs
                        under the Cabinet of Ministers and in April 2025 has been
                        awarded France’s Order of Arts and Literature. Her public
                        service commitment is evident in her dedication to creating
                        opportunities for young people in Uzbekistan’s cultural sector
                        and fostering a cultural economy that unites communities and
                        generations.
                    </p>
                </div>
